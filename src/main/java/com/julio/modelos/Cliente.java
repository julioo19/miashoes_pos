package com.julio.modelos;

public class Cliente {
    private String dni_id;
    private String nombre_cliente;
    private String nro_telefono;

    public String getDni_id() {
        return dni_id;
    }

    public void setDni_id(String dni_id) {
        this.dni_id = dni_id;
    }

    public String getNombre_cliente() {
        return nombre_cliente;
    }

    public void setNombre_cliente(String nombre_cliente) {
        this.nombre_cliente = nombre_cliente;
    }

    public String getNro_telefono() {
        return nro_telefono;
    }

    public void setNro_telefono(String nro_telefono) {
        this.nro_telefono = nro_telefono;
    }

    
}
